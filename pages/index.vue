<template>
    <div class="mt-8">
        <h1 class="text-4xl font-bold">Welcome back to Kanri!</h1>
        <h2 class="ml-1 text-dim-3" v-if="boards.length !== 0">
            Your boards are ready and waiting for you.
        </h2>

        <main id="boards">
            <div
                v-if="boards.length === 0 && loading === false"
                class="flex flex-col justify-center p-2 mt-5 items-left bg-elevation-1 rounded-md"
            >
                <h3 class="text-xl font-bold">So empty here!</h3>
                <span>Create a board to get started with tracking your tasks better.</span>
                <IconArrow />
            </div>

            <div v-else class="flex flex-row flex-wrap mt-5 mb-8 gap-6">
                <nuxt-link
                    v-for="(board, index) in boards"
                    id="board-preview"
                    class="flex flex-col bg-elevation-1 bg-elevation-2-hover rounded-md group"
                    :key="index"
                    :to="'/kanban/' + index"
                >
                    <KanbanBoardPreview :board="board" class="filter group-hover:brightness-125" />
                    <div class="flex flex-row justify-between px-1 py-2">
                        <span class="px-1 text-lg font-semibold">
                            {{ board.title }}
                        </span>
                        <VDropdown :distance="2" placement="bottom-end">
                            <button
                                @click.prevent
                                class="py-0.5 px-1 bg-elevation-3-hover rounded-md"
                            >
                                <DotsHorizontalIcon class="h-6 w-6" />
                            </button>

                            <template #popper class="bg-elevation-1">
                                <div class="flex flex-col">
                                    <button
                                        class="hover:bg-gray-200 px-4 py-1.5"
                                        @click="boardAction(index)"
                                    >
                                        Rename
                                    </button>
                                    <button
                                        class="hover:bg-gray-200 px-4 py-1.5"
                                        @click="deleteBoard(index)"
                                    >
                                        Delete
                                    </button>
                                </div>
                            </template>
                        </VDropdown>
                    </div>
                </nuxt-link>
            </div>
        </main>
    </div>
</template>

<script setup lang="ts">
import { ask } from "@tauri-apps/api/dialog";

import { useTauriStore } from "@/stores/tauriStore";

import { generateUniqueID } from "@/utils/idGenerator.js";
import emitter from "@/utils/emitter.js";

import { DotsHorizontalIcon } from "@heroicons/vue/solid";

import type { Board } from "@/types/kanban-types";

const store = useTauriStore().store;
const boards = ref([]);

const loading = ref(true);

onMounted(async () => {
    emitter.on("createBoard", () => {
        createNewBoard();
    });

    boards.value = (await store.get("boards")) || [];
    console.log("Loaded boards:", boards.value);
    loading.value = false;
});

const boardAction = (board: number) => {
    console.log(board);
};

const createNewBoard = () => {
    // TODO: make a nicer onboarding process with a modal (instead of creating a full placeholder board)

    const board: Board = {
        id: generateUniqueID(),
        title: "New Board",
        columns: [
            {
                id: generateUniqueID(),
                title: "New Column 1",
                cards: [
                    {
                        name: "Example Card 1",
                        description: "",
                    },
                    {
                        name: "Example Card 2",
                        description: "",
                    },
                ],
            },
            {
                id: generateUniqueID(),
                title: "New Column 2",
                cards: [{ name: "Example Card 3", description: "" }],
            },
        ],
    };

    boards.value = [...boards.value, board];
    store.set("boards", boards.value);
};

const deleteBoard = async (index: number) => {
    const yes = await ask(
        `Are you sure you want to delete the board "${boards.value[index].title}"?`,
        { title: "Kanri", type: "warning" }
    );

    if (!yes) return;

    boards.value.splice(index, 1);
    store.set("boards", boards.value);
};
</script>
